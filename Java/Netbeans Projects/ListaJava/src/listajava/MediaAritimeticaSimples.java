public class MediaAritimeticaSimples {
    
    public static void main(String[] args) {
        int media1, media2, somaMedia, media;
        
        media1 = (9 + 8 + 7) / 3;
        media2 = (4 + 5 + 6) / 3;
        somaMedia = media1 + media2;
        media = somaMedia / 2;
        
        System.out.println("Média 1: " + media1 + "Média 2: " + media2 + 
                "Soma das médias: " + somaMedia + "Média total: " + media);
    }
}
